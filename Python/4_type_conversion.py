# Convert string to integer
string_number = "200"
integer_number = int(string_number)
print("Integer value:", integer_number)

# Convert integer to float
float_number = float(integer_number)
print("Float value:", float_number)

# Convert float to string
string_float = str(float_number)
print("String representation of float:", string_float)

# Automatic type conversion during operations
value1 = 10
value2 = 2.5
result = value1 + value2  # value1 is converted to float automatically
print("Result of adding integer and float:", result)
