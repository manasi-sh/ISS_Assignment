# Integer
number_of_students = 30
print("Number of students:", number_of_students)

# String with F-string
course_name = "Computer Science"
semester = 2
print(f"Course: {course_name}, Semester: {semester}")

# Boolean
is_enrolled = True
print("Is enrolled:", is_enrolled)

# List
colors = ["red", "green", "blue", "yellow"]
print("Colors list:", colors)

# Tuple
coordinates = (10, 20)
print("Coordinates:", coordinates)

# Set
unique_ids = {101, 102, 103, 101}
print("Unique IDs:", unique_ids)

# Dictionary
student_info = {
    "roll_number": 101,
    "name": "Student",
    "marks": 85,
    "passed": True
}
print("Student information:", student_info)