SPEED_OF_LIGHT = 299792458  # Integer constant (speed of light in meters per second)

# Example use of constants
radius = 7  # Integer variable (radius of the circle)
circle_area = PI * radius * radius  # Float variable (area of the circle)
print("Area of circle with radius 7:", circle_area)

distance = 150000000  # Integer variable (distance in meters)
time_taken = distance / SPEED_OF_LIGHT  # Float variable (time taken by light)
